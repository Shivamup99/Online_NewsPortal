  <h1 style="color:#990012"><font face="verdana" size="6px">Facebook CEO Mark Zuckerberg addresses privacy protections, gets quizzed on 'shadow profiles' and more in House Committee hearing</font></h1><br>
  <div id="main">

    <center><img src="j5.jpg" width="800px" height="400px"></center><br><br>
<p><font face="cambria" size="4px">
Mark Zuckerberg faced the lawmakers as part of the House Committee hearing regarding data protection and privacy of its users for the second day in a row.<br><br>

Facebook CEO Mark Zuckerberg during his Day 1 testimony before the Senate Judiciary and Commerce Committees/ REUTERSFacebook CEO Mark Zuckerberg during his testimony before the Senate Judiciary and Commerce Committees. Image: Reuters
There were a number of interesting things revealed during the discussion in the nearly five-hour hearing.<br><br>

We went through the hearing and listed out five most significant topics in the exchange between the lawmakers and Facebook CEO. One significant point about today’s hearing in contrast to the hearing from yesterday was how the house members spent less time dwelling on Cambridge Analytica, in favour of focusing on a larger range of topics.<br><br>


During the hearing, Facebook made a clear case about supporting General Data Privacy Regulation (GDPR) that EU introduced. Zuckerberg pointed out that Facebook will roll out ‘GDPR-style’ data protection for Facebook users globally because the regulations have “different important pieces”. This includes offering controls to users, asking for consent and more importantly, putting the controls in front of users. Facebook will also introduce a tool that will help users check their privacy settings for the apps that use Facebook authentication.<br><br>
<center><img src="j6.jpg" width="800px" height="400px"></center><br>
<h2><i>Tracking people on the internet regardless of having a Facebook account</i></h2>

Committee members repeatedly questioned about how Facebook is tracking users around the internet using trackers regardless of the users holding an account with the company. Zuckerberg was also asked if Facebook maintained ‘Shadow profiles’ of internet users who never had an account or the ones who had deleted their Facebook accounts. The CEO added that the reason behind 'shadow profiles' was security, to keep a track of Facebook pages that an internet user was accessing without logging in.<br><br>Without that information, anyone could download all the information from all Facebook pages. The second reason was to serve relevant ads to visitors visiting Facebook. He added that he was not familiar with ‘shadow profiles’ and could not answer the scope of data that Facebook collected about Facebook users or non-users on the internet.
<center><img src="j9.jpg" width="800px" height="400px"></center><br>

<h2><i>Cambridge Analytica data leak affecting Zuckerberg’s own profile</i></h2>

Mark Zuckerberg also revealed that data about his Facebook profile was also scraped by Cambridge Analytica making it more personal.<br><br>

Congress members pushing Facebook for more privacy features and protection for minors<br><br>

Zuckerberg was also asked about Facebook’s Messenger for Kids, about the provisions that the company takes to safeguard them. Lawmakers also asked that why was it's not possible to enforce a policy of ‘no-data-sharing’ until the user reaches 18 years of age. He sidestepped the question by answering on different lines, while not committing to adding any additional privacy protection for minors on the platform.<br><br>
<center><img src="j8.jpg" width="800px" height="400px"></center><br>
<h2><i>Fighting opioid sales on Facebook</i></h2>

The last important topic during the hearing was that of opioids. Selling drugs on Facebook is against the terms and services of the platform in addition to being illegal. Committee members pointed out that a number of people were using Facebook to sell drugs on the platform. Zuckerberg responded by saying that the company will “try to improve its content moderation team” adding that “there are a number of areas of content that” Facebook needs to improve.<br><br>
